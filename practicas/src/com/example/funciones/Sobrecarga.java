package com.example.funciones;

public class Sobrecarga {
    public static void main(String[] args) {
    //Sobrecarga consiste en utilizar una misma función siempre y
        // cuando tengan diferente cantidad o tipo de parámetrosla.
        // Se llamará a aquella que se ajuste a los parametros ingresados.

    }
    static int suma(int num1, int num2) {
        return num1 + num2;
    }
    static int resta(int num1, int num2, int num3) {
        return num1 + num2 + num3;
    }
    static int multiplicacion(int num1, int num2, int num3, int num4) {
        return num1 + num2 + num3 + num4;
    }
     /*
        declarar una fucion anonima
        () -> System.out.println("");
         */

}
